import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import en from "../assets/client/locales/en.mjs";
import zhCN from "../assets/client/locales/zh-CN.mjs";
import zhHant from "../assets/client/locales/zh-Hant.mjs";
import { ALL_TRANSLATION_KEYS, POPUP_LOCALE_KEYS } from "./client-locale-policy.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputDirectory = path.join(root, "assets", "client", "locales");
const locales = Object.freeze({ en, "zh-CN": zhCN, "zh-Hant": zhHant });
const checkOnly = process.argv.includes("--check");

const allTranslations = Object.fromEntries(ALL_TRANSLATION_KEYS.map((key) => [
  key,
  Object.values(locales).map((messages) => requiredMessage(messages, key)),
]));
const popupMessages = Object.fromEntries(Object.entries(locales).map(([locale, messages]) => [
  locale,
  Object.fromEntries(POPUP_LOCALE_KEYS.map((key) => [key, requiredMessage(messages, key)])),
]));

await writeGeneratedModule(
  "all-translations.mjs",
  allTranslations,
  "cross-locale client messages",
);
await writeGeneratedModule(
  "popup.mjs",
  popupMessages,
  "popup locale catalogs",
);

console.log(`${checkOnly ? "Verified" : "Generated"} client locale subsets.`);

function requiredMessage(messages, key) {
  if (!Object.hasOwn(messages, key)) throw new Error(`Missing client locale message: ${key}`);
  return messages[key];
}

async function writeGeneratedModule(fileName, value, label) {
  const source = `// Generated by scripts/generate-client-locales.mjs. Do not edit directly.\nexport default Object.freeze(${JSON.stringify(value)});\n`;
  const output = path.join(outputDirectory, fileName);
  if (checkOnly) {
    const current = await fs.readFile(output, "utf8").catch(() => "");
    if (current !== source) {
      throw new Error(`${path.relative(root, output)} is stale. Run node scripts/generate-client-locales.mjs.`);
    }
    return;
  }
  await fs.mkdir(outputDirectory, { recursive: true });
  await fs.writeFile(output, source, "utf8");
  console.log(`Generated ${label}: ${path.relative(root, output)}`);
}
