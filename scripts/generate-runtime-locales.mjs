import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import en from "../assets/client/locales/en.mjs";
import zhCN from "../assets/client/locales/zh-CN.mjs";
import zhHant from "../assets/client/locales/zh-Hant.mjs";
import { isRuntimeLocaleKey } from "./runtime-locale-policy.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputDirectory = path.join(root, "extension", "core", "runtime-locales");
const locales = Object.freeze({ en, "zh-CN": zhCN, "zh-Hant": zhHant });
const checkOnly = process.argv.includes("--check");

for (const [locale, messages] of Object.entries(locales)) {
  const filtered = Object.fromEntries(Object.entries(messages).filter(([key]) => isRuntimeLocaleKey(key)));
  const source = `// Generated by scripts/generate-runtime-locales.mjs. Do not edit directly.\nexport default Object.freeze(${JSON.stringify(filtered)});\n`;
  const output = path.join(outputDirectory, `${locale}.mjs`);
  if (checkOnly) {
    const current = await fs.readFile(output, "utf8").catch(() => "");
    if (current !== source) throw new Error(`${path.relative(root, output)} is stale. Run node scripts/generate-runtime-locales.mjs.`);
  } else {
    await fs.mkdir(outputDirectory, { recursive: true });
    await fs.writeFile(output, source, "utf8");
  }
}

console.log(`${checkOnly ? "Verified" : "Generated"} ${Object.keys(locales).length} runtime locale catalogs.`);
